#!/usr/bin/env node
/**
 * Install allowlisted model/env keys into Electron userData .env.local.
 * Never prints secret values. Never copies unknown keys.
 *
 * Usage:
 *   node scripts/install-desktop-env.mjs
 *     reads allowlisted keys from process env (and optional --from path)
 *     writes ~/Library/Application Support/FC-OPC iBot/.env.local (mode 0600)
 *
 *   node scripts/install-desktop-env.mjs --from /path/to/source.env
 *     only allowlisted keys from that file (plus process env override)
 *
 * Does NOT read or embed the repo .env.local into the .app.
 */
import fs from "node:fs";
import os from "node:os";
import path from "node:path";
import { createRequire } from "node:module";
import { fileURLToPath } from "node:url";

const require = createRequire(import.meta.url);
const runtime = require("../desktop/runtime.cjs");

const PRODUCT_USER_DATA = path.join(
  os.homedir(),
  "Library",
  "Application Support",
  "FC-OPC iBot",
);

function parseArgs(argv) {
  /** @type {{ from?: string, userData?: string }} */
  const out = {};
  for (let i = 2; i < argv.length; i++) {
    const a = argv[i];
    if (a === "--from" && argv[i + 1]) {
      out.from = argv[++i];
    } else if (a === "--user-data" && argv[i + 1]) {
      out.userData = argv[++i];
    } else if (a === "--help" || a === "-h") {
      out.help = true;
    }
  }
  return out;
}

function main() {
  const args = parseArgs(process.argv);
  if (args.help) {
    console.log(`Usage: node scripts/install-desktop-env.mjs [--from FILE] [--user-data DIR]

Writes allowlisted keys only to <userData>/.env.local with mode 0600.
Allowlist: ${runtime.ALLOWED_ENV_KEYS.join(", ")}
Never prints secret values. Never bundles into .app.
`);
    process.exit(0);
  }

  const userData = args.userData
    ? path.resolve(args.userData)
    : PRODUCT_USER_DATA;
  const dest = path.join(userData, ".env.local");

  let fileContents = "";
  if (args.from) {
    const fromPath = path.resolve(args.from);
    if (!fs.existsSync(fromPath)) {
      console.error("[install-desktop-env] FAIL: --from file not found");
      process.exit(1);
    }
    // Refuse to silently use paths that look like packaging secrets dumps
    fileContents = fs.readFileSync(fromPath, "utf8");
  }

  // Process env wins for allowlisted keys; unknown keys dropped by parse/resolve.
  const merged = runtime.resolveAllowedEnvironment(process.env, fileContents);
  const lines = [
    "# FC-OPC iBot desktop userData env — allowlisted keys only",
    "# Generated by scripts/install-desktop-env.mjs",
    "# Do not commit. Mode 0600.",
    "",
  ];
  let configured = 0;
  for (const key of runtime.ALLOWED_ENV_KEYS) {
    if (merged[key]) {
      lines.push(`${key}=${merged[key]}`);
      configured += 1;
    }
  }
  if (configured === 0) {
    console.error(
      "[install-desktop-env] FAIL: no allowlisted keys found in process env or --from file",
    );
    console.error(
      `  Set one of: ${runtime.ALLOWED_ENV_KEYS.join(", ")}`,
    );
    process.exit(1);
  }

  fs.mkdirSync(userData, { recursive: true });
  fs.writeFileSync(dest, lines.join("\n") + "\n", { mode: 0o600 });
  try {
    fs.chmodSync(dest, 0o600);
  } catch {
    // best-effort on platforms without chmod
  }

  // Presence-only report
  console.log("[install-desktop-env] wrote", dest);
  console.log("[install-desktop-env]", runtime.formatConfigPresence(merged));
  console.log(
    "[install-desktop-env] Restart the .app to pick up config. Never commit this file.",
  );
}

main();
